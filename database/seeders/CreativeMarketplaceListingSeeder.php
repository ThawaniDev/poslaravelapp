     */
    public function run(): void
    {
        $cat    = fn (string $slug) => MarketplaceCategory::where('slug', $slug)->first()?->id;
        $theme  = fn (string $slug) => Theme::where('slug', $slug)->first()?->id;
        $layout = fn (string $key)  => PosLayoutTemplate::where('layout_key', $key)->first()?->id;

        foreach ($this->listings() as $data) {
            $data['pos_layout_template_id'] = $layout($data['_layout_key']);
            $data['theme_id']               = $theme($data['_theme_slug']);
            $data['category_id']            = $cat($data['_category_slug']);

            unset($data['_layout_key'], $data['_theme_slug'], $data['_category_slug']);

            if (! $data['pos_layout_template_id']) {
                continue;
            }

            TemplateMarketplaceListing::updateOrCreate(
                [
                    'pos_layout_template_id' => $data['pos_layout_template_id'],
                    'title'                  => $data['title'],
                ],
                $data,
            );
        }
    }

