    /**
     * Helper to build a listing array with less repetition.
     */
    private function l(
        string $layoutKey, string $themeSlug, string $catSlug,
        string $publisher, string $title, string $titleAr,
        string $desc, string $descAr,
        string $shortDesc, string $shortDescAr,
        MarketplacePricingType $pricing, float $price, ?SubscriptionInterval $interval,
        array $tags, string $version, string $changelog,
        int $downloads, float $rating, int $reviews,
        bool $featured, bool $verified,
        MarketplaceListingStatus $status, int $daysAgo,
    ): array {
        return [
            '_layout_key'            => $layoutKey,
            '_theme_slug'            => $themeSlug,
            '_category_slug'         => $catSlug,
            'publisher_name'         => $publisher,
            'publisher_avatar_url'   => null,
            'title'                  => $title,
            'title_ar'               => $titleAr,
            'description'            => $desc,
            'description_ar'         => $descAr,
            'short_description'      => $shortDesc,
            'short_description_ar'   => $shortDescAr,
            'preview_images'         => [],
            'pricing_type'           => $pricing,
            'price_amount'           => $price,
            'price_currency'         => 'SAR',
            'subscription_interval'  => $interval,
            'tags'                   => $tags,
            'version'                => $version,
            'changelog'              => $changelog,
            'download_count'         => $downloads,
            'average_rating'         => $rating,
            'review_count'           => $reviews,
            'is_featured'            => $featured,
            'is_verified'            => $verified,
            'status'                 => $status,
            'approved_at'            => $status === MarketplaceListingStatus::Approved ? now()->subDays($daysAgo) : null,
            'published_at'           => $status === MarketplaceListingStatus::Approved ? now()->subDays($daysAgo) : null,
        ];
    }
